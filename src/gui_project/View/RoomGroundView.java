    private void formKeyPressed(java.awt.event.KeyEvent evt)//GEN-FIRST:event_formKeyPressed
    {//GEN-HEADEREND:event_formKeyPressed
        //Collision check
        roomCtrl.checkCollisions(evt.getKeyCode(), mainCtrl,
                detectiveCtrl, getBound());

        repaint();
    }//GEN-LAST:event_formKeyPressed

    private void formKeyReleased(java.awt.event.KeyEvent evt)//GEN-FIRST:event_formKeyReleased
    {//GEN-HEADEREND:event_formKeyReleased
        detectiveCtrl.keyReleased(evt);
        Rectangle dogHouseBound = lockedAreaCtrl.getView().getBound();
        Rectangle keyPasswordBound = keyPasswordCtrl.getView().getBound();

        //check doorHouse collision
        if (detectiveCtrl.getView().getBound().intersects(doorHouse.getBounds()))
        {
            mainCtrl.showPanel("House");
            detectiveCtrl.updateGroundHouseLocation();
        } //check lockedArea collision
        else if (detectiveCtrl.getView().getBound().intersects(dogHouseBound)
                && !lockedAreaCtrl.getLockedArea().isUnlocked())
        {
            mainCtrl.showPanel("DogHouseLock");
        } 
        else if (detectiveCtrl.getView().getBound().intersects(keyPasswordBound)
                && !keyPasswordCtrl.getKeyPassword().isCorrect())
        {
            mainCtrl.showPanel("TailOffice");
        }

        repaint();
    }//GEN-LAST:event_formKeyReleased

